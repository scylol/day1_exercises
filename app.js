// Challenge Beyond

function beyond(num){
	if(num === Infinity){
		console.log ("And beyond");
	} 
	else if( num >= 1) {
		console.log("To infinity");
	}
	else if (num < 0) {
		console.log("To negative infinity");
	}
	else if (num === 0) {
		console.log("Staying home");
	}
}

beyond(Infinity);